CREATE TABLE public.otodom_apartments
(
    number_id integer NOT NULL,
    short_id character varying(8) NOT NULL,
    long_id text NOT NULL,
    url text NOT NULL,
    title text NOT NULL,
    price integer NOT NULL,
    advertiser_type text,
    advert_type text,
    utc_created_at timestamp without time zone,
    utc_scraped_at timestamp without time zone,
    description text,
    city text,
    subregion text,
    province text,
    location text,
    latitude real,
    longitude real,
    market text,
    status text,
    building_type text,
    apartment_features text,
    build_year smallint,
    floor smallint,
    building_floors_num smallint,
    apartment_area smallint,
    n_rooms smallint,
    heating text,
    media text,
    PRIMARY KEY (number_id)
);
